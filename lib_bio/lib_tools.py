import os

#=======================================================================================================================
# FILE I/O
#=======================================================================================================================
def create_dir(filepath):
    if os.path.exists(filepath) == False:
        os.makedirs(filepath)

#=======================================================================================================================
# BIT TO STRING CONVERSION
#=======================================================================================================================
def byte_to_bits(value):
    bits = ''    
    
    for i in range(8):
        bits += str(int(value) & 1)
        value = value / 2

    return bits[::-1]
def short_to_bits(value):
    bits = ''    
    
    for i in range(16):
        bits += str(int(value) & 1)
        value = value / 2

    return bits[::-1]
def long_to_bits(value):
    bits = ''    
    
    for i in range(32):
        bits += str(int(value) & 1)
        value = value / 2

    return bits

#=======================================================================================================================
# STRING FORMATTING
#=======================================================================================================================
def fix_id(id):
    return "{0:0>2}".format(id)
    